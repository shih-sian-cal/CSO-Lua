-- 生成武器函式
function G_DBWP(player,pwp,map,wt,pos,wn)
    local wp
    local pu = player.user
    if pwp == 1 then
        wp = Game.Weapon.CreateAndDrop(wt,pos)
    elseif pwp == 0 then
        wp = Game.Weapon.CreateAndDrop(WeaponList[wt],pos)
    end

    if pu.buyUB == 1 then
        local RN = G_RF(1,100)
        if RN > 99.00 and RN < 100.01 then
            wp.infiniteclip = true
            AdviceWPUB.value = player.name
        end
    end

    local wu = wp.user

    if map_weapon[map] ~= nil then
        for i, weapon in ipairs(map_weapon[map]) do
            if wn == weapon.value then
                wp.color = WeaponColor[i]
                break
            end
        end
        wu.dtap = wn
    end
    
    return wp
end

