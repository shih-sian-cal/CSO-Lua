    elseif t == 21 then
        if pu.tap >= hatchNum * (10^32) then
            for i = 1, hatchNum do
                pu.tap = pu.tap - 100000000000000000000000000000000
                pu.hatch = pu.hatch + 1
                if i == 1 then
                    pos = {x = 113, y = -40, z = 3}
                elseif i == 2 then
                    pos = {x = 113, y = -39, z = 3}
                elseif i == 3 then
                    pos = {x = 113, y = -41, z = 3}
                end
                weapon_create = drawWeapon(map_weapon[t], player, 1, t, pos)
            end
            return weapon_create
        else
            AdviceNTAP.value = player.index
        end

    elseif t == 1001 then
        if pu.lc >= hatchNum * 1 then
            for i = 1, hatchNum do
                pu.lc = pu.lc - 1
                pu.hatch = pu.hatch + 1
                if i == 1 then
                    pos = {x = 145, y = -90, z = 3}
                elseif i == 2 then
                    pos = {x = 145, y = -89, z = 3}
                elseif i == 3 then
                    pos = {x = 145, y = -91, z = 3}
                end   
                weapon_create = drawWeapon(map_weapon[t], player, 1, t, pos)
            end
            return weapon_create
        else
            AdviceNLC.value = player.index
        end

    elseif t == 1002 then
        if pu.lc >= hatchNum * 50 then
            for i = 1, hatchNum do
                pu.lc = pu.lc - 50
                pu.hatch = pu.hatch + 1
                if i == 1 then
                    pos = {x = 145, y = 131, z = 3}
                elseif i == 2 then
                    pos = {x = 145, y = 130, z = 3}
                elseif i == 3 then
                    pos = {x = 145, y = 132, z = 3}
                end   
                weapon_create = drawWeapon(map_weapon[t], player, 1, t, pos)
            end
            return weapon_create
        else
            AdviceNLC.value = player.index
        end

    end
end

function OnMapUse(player, mapNumber, mapCoordinate)
    if player.user.autohatch == 1 then
        if player.user.AHmap == 0 then
            player.user.AHmap = mapNumber
            AdviceAHStart.value = player.index
        else
            player.user.AHmap = 0
            AdviceAHStop.value = player.index
        end
    else
        G_WP(player, mapNumber, mapCoordinate)
    end
end

